export function getQuizByDifficulty(
  difficulty?: QuizQuestion["difficulty"]
): QuizQuestion[] {
  if (!difficulty) return SAMPLE_QUIZ;
  return SAMPLE_QUIZ.filter((q) => q.difficulty === difficulty);
}

export function getQuizByCategory(category: QuizQuestion["category"]) {
  return SAMPLE_QUIZ.filter((q) => q.category === category);
}

export function getQuizCount() {
  return SAMPLE_QUIZ.length;
}

export function shuffleQuiz(questions: QuizQuestion[], limit?: number) {
  const arr = [...questions].sort(() => Math.random() - 0.5);
  return limit ? arr.slice(0, limit) : arr;
}
